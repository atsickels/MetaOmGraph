package edu.iastate.metnet.metaomgraph.model;

public class InfoModel {

	private String type;
	private String last;
	private String asPercent;
	private String value;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAsPercent() {
		return asPercent;
	}
	public void setAsPercent(String asPercent) {
		this.asPercent = asPercent;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getLast() {
		return last;
	}
	public void setLast(String last) {
		this.last = last;
	}
	
	
}
